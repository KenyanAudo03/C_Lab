#include <stdio.h>

int main()
{

    int myNum = 15;          //  Integers
    float myFloatNum = 5.99; // Floating point number
    char myLetter = 'D';     // Character

    printf("Hello, world! \n"); // String
    printf("%d \n", myNum);
    printf("%f\n", myFloatNum);
    printf("%c\n", myLetter);
    return 0;
}